a = 10
b = 3
print("Arithmetic Operators:")
addition = a + b         # Arithmetic operators
print("Addition:", addition)
subtraction = a - b
print("Subtraction:", subtraction)
multiplication = a * b
print("Multiplication:", multiplication)
division = a / b
print("Division:", division)
modulo = a % b
print("Modulo:", modulo)
exponentiation = a ** b
print("Exponentiation:", exponentiation)
print("\nComparison Operators:")
greater_than = a > b                    # Comparison operators
print("Greater than:", greater_than)
less_than = a < b
print("Less than:", less_than)
greater_than_or_equal = a >= b
print("Greater than or equal to:", greater_than_or_equal)
less_than_or_equal = a <= b
print("Less than or equal to:", less_than_or_equal)
equal_to = a == b
print("Equal to:", equal_to)
not_equal_to = a != b
print("Not equal to:", not_equal_to)
print("\nLogical Operators:")
logical_and = (a > 0) and (b > 0)                 # Logical operators
print("Logical AND:", logical_and)
logical_or = (a > 0) or (b > 0)
print("Logical OR:", logical_or)
logical_not_a = not (a > 0)
print("Logical NOT (a):", logical_not_a)
logical_not_b = not (b > 0)
print("Logical NOT (b):", logical_not_b)
print("\nAssignment Operators:")
x = 5                # Assignment operators
x += 3
print("x:", x)
y = 10
y -= 2
print("y:", y)
z = 7
z *= 2
print("z:", z)
print("\nBitwise Operators:")
bitwise_and = a & b            # Bitwise operators
print("Bitwise AND:", bitwise_and)
bitwise_or = a | b
print("Bitwise OR:", bitwise_or)
bitwise_xor = a ^ b
print("Bitwise XOR:", bitwise_xor)
bitwise_not_a = ~a
print("Bitwise NOT (a):", bitwise_not_a)
bitwise_shift_left = a << 2
print("Bitwise Shift Left:", bitwise_shift_left)
bitwise_shift_right = a >> 1
print("Bitwise Shift Right:", bitwise_shift_right)